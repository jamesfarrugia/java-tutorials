/**
 * Hello world program.  Prints hello world to the screen and exits
 */
public class HelloWorldCharArray
{
	/**
	 * Main method that prints the message
	 * 
	 * @param args array with command line arguments
	 */
	public static void main(String[] args)
	{
		// Declaring the text as an array of characters
		char[] myTextArray = new char[]{'H', 'e', 'l', 'l', 'o', ' ', 'W', 'o', 'r', 'l', 'd', '!'};
		// Prints out "Hello World!"
		System.out.println(myTextArray);
	}
}
